// Express: Framework web para Node.js que permite crear servidores HTTP
const express = require('express');

// CORS: Middleware que permite Cross-Origin Resource Sharing
// Permite que la API sea accesible desde otros dominios/puertos
const cors = require('cors');

// Variables de entorno
require('dotenv').config();

// WhatsApp Web.js: Biblioteca que conecta con WhatsApp Web
// Client: Clase principal para manejar la conexi√≥n
// LocalAuth: Estrategia de autenticaci√≥n que guarda la sesi√≥n localmente
const { Client, LocalAuth } = require('whatsapp-web.js');

// IA Simple: Asistente virtual para respuestas autom√°ticas
const SimpleAI = require('./ai/SimpleAI');

// Base de datos MongoDB
const { mongoConnection } = require('./database/mongodb');
const whatsappDB = require('./services/whatsappDB');

// ================= INICIALIZACI√ìN DE VARIABLES =================

// Crear instancia de la aplicaci√≥n Express
const app = express();

// Puerto donde correr√° el servidor (3000 es el puerto est√°ndar para desarrollo)
const PORT = process.env.PORT || 3000;

// Variable global para almacenar la instancia del cliente de WhatsApp
// null significa que a√∫n no se ha inicializado
let whatsappClient = null;

// Variable de estado que indica si WhatsApp est√° conectado y listo
// false por defecto hasta que se complete la autenticaci√≥n
let isClientReady = false;

// Variable de estado para MongoDB
let isMongoConnected = false;

// Instancia de la IA para respuestas autom√°ticas
const aiBot = new SimpleAI();

// Variable para controlar si la IA est√° activada
let aiEnabled = true;

// ================= CONFIGURACI√ìN DE MIDDLEWARES =================

// CORS: Habilita el acceso desde cualquier origen (*)
// Permite que aplicaciones web desde otros dominios usen esta API
app.use(cors());

// JSON Parser: Middleware que convierte el cuerpo de las peticiones HTTP
// de formato JSON a objetos JavaScript accesibles via req.body
app.use(express.json());

// ================= FUNCI√ìN DE INICIALIZACI√ìN DE MONGODB =================

const initializeMongoDB = async () => {
    try {
        console.log('üîÑ Inicializando conexi√≥n a MongoDB...');
        await mongoConnection.connect();
        isMongoConnected = true;
        console.log('‚úÖ MongoDB conectado exitosamente');
    } catch (error) {
        console.error('‚ùå Error conectando a MongoDB:', error.message);
        isMongoConnected = false;
        // Continuar sin MongoDB (modo degradado)
        console.log('‚ö†Ô∏è  Continuando en modo sin base de datos');
    }
};

// ================= FUNCI√ìN DE INICIALIZACI√ìN DE WHATSAPP =================

const initializeWhatsApp = () => {
    // Log de inicio del proceso de inicializaci√≥n
    console.log('üîÑ Inicializando cliente de WhatsApp para API...');
    
    // Crear nueva instancia del cliente de WhatsApp
    whatsappClient = new Client({
        // Estrategia de autenticaci√≥n local
        authStrategy: new LocalAuth({
            // Ruta donde se guardar√°n los datos de autenticaci√≥n
            // Esto permite que la sesi√≥n persista entre reinicios
            dataPath: '/app/.wwebjs_auth'
        }),
        
        // Configuraci√≥n de Puppeteer (controla el navegador Chrome)
        puppeteer: {
            // Modo headless: ejecuta Chrome sin interfaz gr√°fica
            headless: true,
            
            // Argumentos espec√≠ficos para Chrome en Docker
            args: [
                '--no-sandbox',                    // Desactiva sandbox de seguridad (necesario en Docker)
                '--disable-setuid-sandbox',        // Desactiva sandbox setuid
                '--disable-dev-shm-usage',         // Usa /tmp en lugar de /dev/shm (evita problemas de memoria)
                '--disable-accelerated-2d-canvas', // Desactiva aceleraci√≥n de canvas 2D
                '--no-first-run',                  // Evita el wizard de primera ejecuci√≥n
                '--no-zygote',                     // Desactiva proceso zygote
                '--disable-gpu',                   // Desactiva GPU (no disponible en headless)
                '--disable-background-timer-throttling',     // Evita throttling de timers
                '--disable-backgrounding-occluded-windows',  // Evita optimizaciones de ventanas ocultas
                '--disable-renderer-backgrounding',          // Evita backgrounding del renderer
                '--disable-web-security',          // Desactiva seguridad web (solo para desarrollo)
                '--disable-features=TranslateUI',  // Desactiva interfaz de traducci√≥n
                '--disable-ipc-flooding-protection' // Desactiva protecci√≥n de flooding IPC
            ]
        }
    });

    // ================= EVENT LISTENERS DEL CLIENTE =================

    // Evento: Se genera un c√≥digo QR para autenticaci√≥n
    whatsappClient.on('qr', qr => {
        console.log('üì± C√≥digo QR generado. Escanea con tu WhatsApp:');
        // Imprime el c√≥digo QR como texto (para debug, no se ve como QR visual)
        console.log(qr);
    });

    // Evento: Cliente listo para usar (autenticado y conectado)
    whatsappClient.on('ready', async () => {
        console.log('üöÄ ¬°Cliente de WhatsApp API listo!');
        // Cambiar estado global a conectado
        isClientReady = true;
        
        // Sincronizar chats con MongoDB si est√° conectado (sin bloquear)
        if (isMongoConnected) {
            console.log('üîÑ Iniciando sincronizaci√≥n de chats con MongoDB...');
            
            // Ejecutar sincronizaci√≥n en background
            setTimeout(async () => {
                try {
                    const chats = await whatsappClient.getChats();
                    console.log(`üìä Sincronizando ${chats.length} chats con MongoDB...`);
                    
                    let syncCount = 0;
                    for (const chat of chats) {
                        try {
                            const chatData = {
                                chatId: chat.id._serialized,
                                name: chat.name,
                                isGroup: chat.isGroup,
                                archived: chat.archived,
                                pinned: chat.pinned,
                                unreadCount: chat.unreadCount
                            };
                            
                            // Agregar informaci√≥n de grupo si aplica
                            if (chat.isGroup && chat.groupMetadata) {
                                chatData.groupMetadata = {
                                    creation: chat.groupMetadata.creation,
                                    owner: chat.groupMetadata.owner?._serialized,
                                    description: chat.groupMetadata.desc,
                                    descriptionOwner: chat.groupMetadata.descOwner?._serialized,
                                    descriptionTime: chat.groupMetadata.descTime,
                                    participantsCount: chat.participants?.length || 0,
                                    participants: chat.participants?.map(p => ({
                                        id: p.id._serialized,
                                        isAdmin: p.isAdmin,
                                        isSuperAdmin: p.isSuperAdmin
                                    })) || []
                                };
                            }
                            
                            await whatsappDB.saveOrUpdateChat(chatData);
                            syncCount++;
                        } catch (chatError) {
                            console.error(`‚ùå Error sincronizando chat ${chat.name}:`, chatError.message);
                        }
                    }
                    
                    console.log(`‚úÖ ${syncCount} chats sincronizados con MongoDB`);
                } catch (error) {
                    console.error('‚ùå Error en sincronizaci√≥n de chats:', error.message);
                }
            }, 2000); // Esperar 2 segundos antes de sincronizar
        }
    });

    // Evento: Autenticaci√≥n exitosa
    whatsappClient.on('authenticated', () => {
        console.log('‚úÖ Cliente autenticado correctamente');
    });

    // Evento: Error de autenticaci√≥n
    whatsappClient.on('auth_failure', msg => {
        console.error('‚ùå Error de autenticaci√≥n:', msg);
        // Marcar como no conectado en caso de error
        isClientReady = false;
    });

    // Evento: Cliente desconectado
    whatsappClient.on('disconnected', (reason) => {
        console.log('üì§ Cliente desconectado:', reason);
        // Marcar como no conectado
        isClientReady = false;
    });

    // Evento: Mensaje recibido
    whatsappClient.on('message', async msg => {
        try {
            // ================= IDENTIFICAR TIPO DE CHAT =================
            
            // M√©todo 1: Verificar por el formato del ID
            const isFromGroup = msg.from.endsWith('@g.us');
            
            // M√©todo 2: Obtener informaci√≥n del chat (m√°s confiable)
            const chat = await msg.getChat();
            const isGroupChat = chat.isGroup;
            
            // Log del mensaje recibido con informaci√≥n del tipo de chat
            const chatType = isGroupChat ? 'GRUPO' : 'INDIVIDUAL';
            console.log(`üì® [${chatType}] Mensaje recibido de ${msg.from}: "${msg.body}"`);
            
            // Si es grupo, obtener informaci√≥n adicional
            if (isGroupChat) {
                console.log(`üë• [GRUPO] Nombre: "${chat.name}"`);
                console.log(`üë§ [GRUPO] Remitente: ${msg.author || 'Desconocido'}`);
            }
            
            // ================= GUARDAR EN MONGODB =================
            
            if (isMongoConnected) {
                try {
                    // Preparar datos del mensaje para MongoDB
                    const messageData = {
                        messageId: msg.id._serialized,
                        chatId: msg.from,
                        from: msg.from,
                        author: msg.author || null,
                        to: msg.to,
                        body: msg.body || '', // Valor por defecto para mensajes sin texto
                        type: msg.type || 'text',
                        timestamp: new Date(msg.timestamp * 1000),
                        fromMe: msg.fromMe,
                        hasMedia: msg.hasMedia,
                        isGroup: isGroupChat,
                        chatName: chat.name,
                        isForwarded: msg.isForwarded || false,
                        isStatus: msg.isStatus || false,
                        deviceType: msg.deviceType || null
                    };
                    
                    // Solo mostrar log para mensajes con contenido o media
                    if (messageData.body || messageData.hasMedia) {
                        const contentPreview = messageData.body ? 
                            messageData.body.substring(0, 50) + (messageData.body.length > 50 ? '...' : '') :
                            `[${messageData.type.toUpperCase()}]`;
                        console.log(`üíæ [DB] Guardando: ${chatType} - ${contentPreview}`);
                    }
                    
                    // Guardar mensaje en MongoDB
                    await whatsappDB.saveMessage(messageData);
                    
                    // Actualizar informaci√≥n del chat si es necesario
                    const chatData = {
                        chatId: chat.id._serialized,
                        name: chat.name,
                        isGroup: chat.isGroup,
                        archived: chat.archived,
                        pinned: chat.pinned,
                        unreadCount: chat.unreadCount
                    };
                    
                    await whatsappDB.saveOrUpdateChat(chatData);
                    
                } catch (dbError) {
                    console.error('‚ùå [DB] Error guardando mensaje:', dbError.message);
                }
            }
            
            // ================= RESPUESTA AUTOM√ÅTICA CON IA =================
            
            // Solo responder si la IA est√° activada y el mensaje no es de nosotros
            if (aiEnabled && !msg.fromMe) {
                
                // Configurar diferentes comportamientos para grupos vs individuales
                let shouldRespond = true;
                
                if (isGroupChat) {
                    // En grupos, solo responder si mencionan al bot o usan palabras clave
                    const mentionKeywords = ['bot hablame']; //['bot', 'asistente', 'ayuda', 'help'];
                    const messageText = msg.body.toLowerCase();
                    shouldRespond = mentionKeywords.some(keyword => messageText.includes(keyword));
                    
                    console.log(`ü§ñ [GRUPO] ${shouldRespond ? 'Responder√°' : 'No responder√°'} (keywords: ${mentionKeywords.join(', ')})`);
                }
                
                if (shouldRespond) {
                    // Obtener informaci√≥n del contacto (nombre si est√° disponible)
                    const contact = await msg.getContact();
                    const senderInfo = {
                        name: contact.pushname || contact.name || null,
                        number: msg.from,
                        isGroup: isGroupChat,
                        groupName: isGroupChat ? chat.name : null,
                        author: msg.author || null // En grupos, qui√©n envi√≥ el mensaje
                    };
                    
                    // Generar respuesta con la IA
                    const aiResponse = await aiBot.generateResponse(msg.body, senderInfo);
                    
                    // Enviar respuesta autom√°tica
                    await msg.reply(aiResponse);
                    
                    console.log(`ü§ñ [${chatType}] Respuesta enviada a ${msg.from}: "${aiResponse}"`);
                    
                    // ================= GUARDAR RESPUESTA IA EN MONGODB =================
                    
                    if (isMongoConnected) {
                        try {
                            // Determinar palabras clave que activaron la respuesta (para grupos)
                            const activatedKeywords = [];
                            if (isGroupChat) {
                                const mentionKeywords = ['bot hablame'];
                                const messageText = msg.body.toLowerCase();
                                activatedKeywords.push(...mentionKeywords.filter(keyword => messageText.includes(keyword)));
                            }
                            
                            // Actualizar el mensaje original con informaci√≥n de la respuesta IA
                            await whatsappDB.updateMessageWithAIResponse(msg.id._serialized, {
                                responseText: aiResponse,
                                keywords: activatedKeywords,
                                category: isGroupChat ? 'group_triggered' : 'individual_auto'
                            });
                            
                        } catch (dbError) {
                            console.error('‚ùå [DB] Error actualizando respuesta IA:', dbError.message);
                        }
                    }
                }
            }
            
        } catch (error) {
            console.error('‚ùå Error procesando mensaje:', error);
        }
    });

    // Inicializar el cliente y manejar errores
    whatsappClient.initialize().catch(error => {
        console.error('‚ùå Error inicializando cliente:', error);
    });
};

